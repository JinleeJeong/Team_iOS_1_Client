import UIKit
extension UIColor {

  @nonobjc class var heather: UIColor {
    return UIColor(red: 181.0 / 255.0, green: 121.0 / 255.0, blue: 186.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var saffron: UIColor {
    return UIColor(red: 1.0, green: 166.0 / 255.0, blue: 8.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var softBlue: UIColor {
    return UIColor(red: 107.0 / 255.0, green: 180.0 / 255.0, blue: 226.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var brownGrey: UIColor {
    return UIColor(white: 168.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var white: UIColor {
    return UIColor(white: 1.0, alpha: 1.0)
  }

  @nonobjc class var black: UIColor {
    return UIColor(white: 0.0, alpha: 1.0)
  }

  @nonobjc class var veryLightPink: UIColor {
    return UIColor(white: 206.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var paleGrey: UIColor {
    return UIColor(red: 239.0 / 255.0, green: 239.0 / 255.0, blue: 244.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var brownishGrey: UIColor {
    return UIColor(white: 100.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var hospitalGreen: UIColor {
    return UIColor(red: 161.0 / 255.0, green: 216.0 / 255.0, blue: 154.0 / 255.0, alpha: 1.0)
  }
    
    @nonobjc class var orangeyRed: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 59.0 / 255.0, blue: 59.0 / 255.0, alpha: 1.0)
    }
    
    

}
